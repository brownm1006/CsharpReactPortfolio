        ('CHEVROLET', 'SILVERADO', NULL, 'SILVERADO'),
        ('CHEVROLET', 'EQUINOX', NULL, 'EQUINOX')
),
model_years(model_year) AS (
    SELECT generate_series(
        EXTRACT(YEAR FROM CURRENT_DATE)::integer + 1,
        EXTRACT(YEAR FROM CURRENT_DATE)::integer - 6,
        -1
    )
)
INSERT INTO quote.vehicle_models (manufacturer_id, model_year, model_name, trim, vehicle_code)
SELECT
    manufacturers.id,
    model_years.model_year,
    seeded_models.model_name,
    seeded_models.trim,
    seeded_models.vehicle_code
FROM seeded_models
JOIN quote.vehicle_manufacturers manufacturers
    ON manufacturers.name = seeded_models.manufacturer_name
CROSS JOIN model_years
ON CONFLICT ON CONSTRAINT uq_vehicle_models_model DO UPDATE SET
    vehicle_code = EXCLUDED.vehicle_code,
    is_active = true;

