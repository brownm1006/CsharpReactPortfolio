CREATE OR REPLACE FUNCTION quote.set_updated_at_utc()
RETURNS trigger AS $$
BEGIN
    NEW.updated_at_utc = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_quotes_set_updated_at ON quote.quotes;
CREATE TRIGGER trg_quotes_set_updated_at
BEFORE UPDATE ON quote.quotes
FOR EACH ROW EXECUTE FUNCTION quote.set_updated_at_utc();

DROP TRIGGER IF EXISTS trg_quote_drivers_set_updated_at ON quote.quote_drivers;
CREATE TRIGGER trg_quote_drivers_set_updated_at
BEFORE UPDATE ON quote.quote_drivers
FOR EACH ROW EXECUTE FUNCTION quote.set_updated_at_utc();

DROP TRIGGER IF EXISTS trg_quote_vehicles_set_updated_at ON quote.quote_vehicles;
