BEFORE UPDATE ON quote.quote_vehicles
FOR EACH ROW EXECUTE FUNCTION quote.set_updated_at_utc();

DROP TRIGGER IF EXISTS trg_quote_vehicle_usages_set_updated_at ON quote.quote_vehicle_usages;
CREATE TRIGGER trg_quote_vehicle_usages_set_updated_at
BEFORE UPDATE ON quote.quote_vehicle_usages
FOR EACH ROW EXECUTE FUNCTION quote.set_updated_at_utc();

